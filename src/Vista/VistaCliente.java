    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        int fila = jTableCliente.getSelectedRow();
            if (fila >= 0) {
                int id = Integer.parseInt(jTableCliente.getValueAt(fila, 0).toString());
                clienteControlador.eliminarCliente(id);
                cargarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla para eliminar.");
            }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
         Cliente cl = cargarDatosTabla();
            clienteControlador.guardarCliente(cl);
            cargarTabla();
            limpiarCampos();
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        int fila = jTableCliente.getSelectedRow();
            if (fila >= 0) {
                Cliente c = capturarDatos();
                c.setIdCliente(Integer.parseInt(jTableCliente.getValueAt(fila, 0).toString()));
                clienteControlador.actualizarCliente(c);
                cargarDatosTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla para editar.");
            }
    }//GEN-LAST:event_jButtonEditarActionPerformed


