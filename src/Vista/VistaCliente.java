    private void cargarDatosTabla(){
        ClienteControlador clienteControlador = new ClienteControlador();
        List<Cliente> cliente = clienteControlador.obtenerTodosClientes();
        if (cliente != null) {
            DefaultTableModel model = (DefaultTableModel) jTableCliente.getModel();
        model.setRowCount(0);
        
        for (Cliente cl : cliente){
        Object[] row = {
            cl.getIdCliente(),
            cl.getCedula(),
            cl.getNombre(),
            cl.getApellido(),
            cl.getTelefono(),
            cl.getDireccion(),
            cl.getEmail(),
            cl.getLicencia()
                };
        model.addRow(row);
            }
        }

}
