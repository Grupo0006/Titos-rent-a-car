    private void cargarDatosTabla(){
        List<Empleado> empleados = empleadoControlador.obtenerTodosEmpleados();
        if(empleados != null){
            DefaultTableModel model = (DefaultTableModel) TablaEmpleados.getModel();
            model.setRowCount(0);
            
            for(Empleado emp : empleados){
                Object[] row = {
                    emp.getId_Empleado(),
                    emp.getCedula(),
                    emp.getNombre1(),
                    emp.getNombre2(),
                    emp.getApellido1(),
                    emp.getApellido2(),
                    emp.getDireccion(),
                    emp.getEmail()
                };
                model.addRow(row);
            }
        }
    }
